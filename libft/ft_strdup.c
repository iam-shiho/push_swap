#include "push_swap.h"

char	*ft_strdup(const char *s)
{
	size_t			len;
	char			*dest;
	unsigned int	i;

	i = 0;
	len = ft_strlen(s);
	dest = (char *)malloc(sizeof(char) * (len + 1));
	if (!dest)
		return (NULL);
	while (i < len)
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
